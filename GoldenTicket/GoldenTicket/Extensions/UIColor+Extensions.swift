import UIKit

extension UIColor {
    
    @nonobjc class var peach: UIColor {
        return UIColor(red: 253.0 / 255.0, green: 164.0 / 255.0, blue: 122.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var maize: UIColor {
        return UIColor(red: 250.0 / 255.0, green: 217.0 / 255.0, blue: 82.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var darkCream: UIColor {
        return UIColor(red: 251.0 / 255.0, green: 246.0 / 255.0, blue: 149.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownGrey: UIColor {
        return UIColor(white: 159.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var black16: UIColor {
        return UIColor(white: 0.0, alpha: 0.16)
    }

    @nonobjc class var blueGrey: UIColor {
        return UIColor(red: 142.0 / 255.0, green: 142.0 / 255.0, blue: 147.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 211.0 / 255.0, alpha: 1.0)
    }

}
